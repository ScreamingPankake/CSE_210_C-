
using System.ComponentModel.Design;

class Customer
{

    public string _name;
    public string _adress;
    public string _city;
    public string _country;

    public Customer(string name, string adress, string city, string country)
    {
        _name = name;
        _adress = adress;
        _city = city;
        _country = country;
    }

}